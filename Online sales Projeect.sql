-- List all orders with customer names
SELECT o.order_id, c.name, o.order_date, o.total_amount
FROM orders o
JOIN customers c ON o.customer_id = c.customer_id;


-- Total orders and revenue by each customer
SELECT c.name, COUNT(o.order_id) AS total_orders, SUM(o.total_amount) AS total_spent
FROM customers c
JOIN orders o ON c.customer_id = o.customer_id
GROUP BY c.name
ORDER BY total_spent DESC;

-- Top 5 best-selling products
SELECT p.name, SUM(oi.quantity) AS total_units_sold
FROM products p
JOIN order_items oi ON p.product_id = oi.product_id
GROUP BY p.name
ORDER BY total_units_sold DESC
LIMIT 5;

-- Most preferred payment method
SELECT method, COUNT(*) AS count
FROM payments
GROUP BY method
ORDER BY count DESC;

-- Products low in stock (less than 100 units)
SELECT * FROM products
WHERE stock_quantity < 100;

-- Total stock value (inventory worth)
SELECT SUM(price * stock_quantity) AS total_stock_value FROM products;



-- Create a view for sales report
CREATE VIEW sales_report AS
SELECT 
    o.order_id,
    c.name AS customer_name,
    o.order_date,
    oi.quantity,
    p.name AS product_name,
    oi.price,
    oi.quantity * oi.price AS total_price
FROM orders o
JOIN customers c ON o.customer_id = c.customer_id
JOIN order_items oi ON o.order_id = oi.order_id
JOIN products p ON oi.product_id = p.product_id;

